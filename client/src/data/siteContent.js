export function generateAiResponse(question) {
  const normalizedQuestion = question.trim().toLowerCase();

  if (!normalizedQuestion) {
    return "Ask anything about the platform, the learning flow, teacher visibility, or the desktop-first experience.";
  }

  if (
    normalizedQuestion.includes("teacher") ||
    normalizedQuestion.includes("monitor") ||
    normalizedQuestion.includes("homework")
  ) {
    return "Teachers can review completion, time on task, repeated attempts, and weak-area patterns before class. The goal is to make support more practical and less dependent on guesswork.";
  }

  if (
    normalizedQuestion.includes("reading") ||
    normalizedQuestion.includes("listening") ||
    normalizedQuestion.includes("writing")
  ) {
    return "Yes. The platform is designed to prioritize one skill at a time, so learners can focus on the bottleneck that is actually slowing score growth instead of mixing everything immediately.";
  }

  if (
    normalizedQuestion.includes("6.5") ||
    normalizedQuestion.includes("score") ||
    normalizedQuestion.includes("improve")
  ) {
    return "The product is built around measurable progress toward a target score. It combines controlled practice, behavior tracking, and clearer feedback so improvement feels more reliable over time.";
  }

  if (
    normalizedQuestion.includes("desktop") ||
    normalizedQuestion.includes("mobile") ||
    normalizedQuestion.includes("phone")
  ) {
    return "The first release is intentionally desktop-first. Small screens show a branded fallback because the dashboards and sticky learning views are designed for laptop and desktop use.";
  }

  if (
    normalizedQuestion.includes("trial") ||
    normalizedQuestion.includes("pricing") ||
    normalizedQuestion.includes("cost")
  ) {
    return "The interface currently presents a free-trial path to explore the workflow. Pricing details can be collected later, but the product experience is positioned as a premium structured support platform.";
  }

  return "Clarity IELTS is a desktop-first IELTS preparation platform built for adults who need clear diagnostics, structured practice, and teacher collaboration. If you want, ask about a skill, a workflow, or how progress is measured.";
}
